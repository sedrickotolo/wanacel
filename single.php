            <h2 class="my-4">A Ray Of Hope For kwania Flood Victims As Wanacel Foundation Donates Assorted Items
              </h2>
              <h6 class="my-4">15th May 2022,Kwania District</h6>
            <p class="lead">For decades, Lango sub region was the food basket that feeds other regions of Uganda largely because it is blessed with vast acres of flat arable land and good weather that favors the growth of nearly all crops.
             <p class="lead">As such, hunger and food insecurity was on a minimal scale as most families produced enough food for home consumption and some for commercial purposes, but with the disastrous impact of global warming, climate change and land degradation as a result of human activities such as depletion of trees and wetland encroachment among others, the trajectory is already changing and many families are now facing hunger.</p>
            <p class="lead">This comes at a time when Uganda is also faced with the challenge of rising commodity prices which means buying cereals from the market for families faced with food shortages is not even a feasible solution since many households do not have the purchasing power. This is the point where interventions from Wanacel Foundation become critical.</p>
             <p class="lead">“We are alive to the fact that our communities in this region continue to grapple with numerous challenges that call for a multipronged humanitarian intervention, and this explains why we conceived the idea of Wanacel Foundation about six years ago with specific focus on women and the girl child as a vulnerable group in the society” says Patricia Amne Aceng, the executive Director of Wanacel Foundation.
              On 15th May 2022, it was all smiles for the community of Apiibor village in Ayabi town council in Kwania district as Wanacel Foundation delivered a donation worth shillings two million (900 US Dollars).The donation comprised of posho, sugar, assorted clothes and shoes for children.
              <p class="lead">Wanacel Foundation is a Women Advocacy for National Accountability, Equity and Liberty NGO (Non-Governmental Organization) founded on 4th/4/2016. The organization operates in the Northern Uganda Districts of Lira, Gulu and Moroto. Lira is the regional head office. With a clear vision of creating a self-sustaining generation of women, girls and child headed families</p>
              <div>
                <img style="height: 400px; padding: 15px; margin: 15px" src="images/wanacel_april_02.jpg">
              </div>
              <h3 class="lead my-4"></p>Why Kwania District?</h3>
              <p class="lead">Lango sub region currently has a projected population of three million people spread across the districts of Dokolo, Amolatar, Oyam, Otuke, Kole, Kwania, Alebtong, Apac, Apac Municipality and Lira city. Much as the community in these districts experience common challenges, some have peculiar problems that calls for urgent interventions and this explains why Wanacel Foundation instituted immediate response to the plights of the communities in kwania district.
              <p class="lead">Kwania district lies in the basin of Lake Kwania and as it is synonymous with most low lands, the area is susceptible to flooding most times and with such occurrences, many households are often displaced, their crop plantations submerge in water thus exposing to shocks and risks including food insecurity as the area LCIII chairperson Richard Eluk Explains.</p>
              <p class="lead">Eluk says the recent floods have devastated the economic livelihood of the community which is heavily dependent on agriculture and most households can hardly afford meals and support from organizations such as Wanacel Foundation comes in handy.
              “ We recently experienced heavy flooding that displaced some of the households and many lost their crops and although we have received some relief support from government through the ministry of disaster preparedness  ,it is not sufficient to meet the needs of  the affected family.</p>

              <p class="lead">It is against this background that Wanacel Foundation identified Kwania district as an area with a needy community that required expeditious support and intervention.</p>
              <p class="lead">According to Patricia Aceng, the executive Director of Wanacel Foundation, the community in Kwania district was in dire need of support as most families and food insecure.</p>
              <p class="lead">“As an organization, our attention was drawn to the plights of this community as we realized that some families were going for days on empty stomachs yet some are women with children and we were deeply concerned which prompted Wanacel to mobilize resources to support them” says Aceng.</p>

              <p class="lead">The ED says with resources allowing, the organization will cascade her support further to the community in kwania district especially supporting girls in schools with sanitary pads.</p>








